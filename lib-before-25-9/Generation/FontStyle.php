<?php

namespace Aspose\Barcode\Generation;

/**
 * Specifies style information applied to text.
 */
final class FontStyle
{
    /**
     * Normal text
     */
    const REGULAR = 0;
    /**
     * Bold text
     */
    const BOLD = 1;
    /**
     * Italic text
     */
    const ITALIC = 2;
    /**
     * Underlined text
     */
    const UNDERLINE = 4;
    /**
     * Text with a line through the middle
     */
    const STRIKEOUT = 8;
}